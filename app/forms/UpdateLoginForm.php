<?php


namespace app\forms;


use Exception;
use Nette\Forms\Form;

class UpdateLoginForm extends FormFactory
{

    /**
     * @var Form $form
     */
    private Form $form;

    /**
     * FullSignUp constructor.
     */
    public function __construct()
    {
        $this->form = parent::getBootstrapForm("UpdateLoginForm");
    }

    /**
     * @param callable $onSuccess
     *
     * @return Form
     */
    public function create(callable $onSuccess): Form
    {
        $this->form->addPassword("login", "Password for administration of web:")
            ->setRequired(true);
        $this->form->addSubmit("submit", "Update password");

        //var_dump($this->form);
        if ($this->form->isSuccess()) {
            $values = $this->form->getValues("array");
            try {
                $onSuccess($values);
            } catch (Exception $exception) {
                $this->form->addError($exception->getMessage());
            }
        }

        return $this->form;
    }
}